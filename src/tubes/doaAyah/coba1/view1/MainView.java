    private void orderActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderActionPerformed
        // TODO add your handling code here:
        
        String order = ygDiorder.getText();
        try {
            openDb();
            rs = s.executeQuery ("SELECT * FROM tabelbarang WHERE id='" + order + "'");
            while (rs.next()) {
                String id = rs.getString("id");
                String quantity = rs.getString("quantity");
                String nama = rs.getString("nama");
                int harga = rs.getInt("harga");
                try {
                        openDb();
                        s.executeUpdate(
                                "INSERT INTO tabeltransaksi VALUE ('" + id + "','" + quantity + "','" + nama + "','"+ harga +"')"
                        );
                        Object[] row = {id,quantity,nama,harga};
                        model2.addRow(row);

                        JOptionPane.showMessageDialog(null, "Data added!");

                    }catch(SQLException | ClassNotFoundException ex) {
                        ex.printStackTrace();
                        JOptionPane.showMessageDialog(null, ex.getLocalizedMessage());

                    }finally {
                        closeDb();
                    }
                
            }

        }catch(SQLException | ClassNotFoundException ex) {
            ex.printStackTrace();

        }
        boolean isClicked = true;
        if(isClicked == true) {
            isClicked = false;
            JOptionPane.showMessageDialog(null, "Pesanan anda sudah di tambahkan");
            mainPanel.removeAll();
            mainPanel.repaint();
            mainPanel.revalidate();

            // add panel
            mainPanel.add(TransactionPanel);
            mainPanel.repaint();
            mainPanel.revalidate();
        } else {
            //alert the user that the frame is already open
            //I recommend a JOptionPane such as this
            JOptionPane.showMessageDialog(null, "This window is already open");
        }
        
        totalHarga();
    }//GEN-LAST:event_orderActionPerformed

