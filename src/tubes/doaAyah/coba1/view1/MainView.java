    public void loadData(){
        model3.getDataVector().removeAllElements();
        try {
            openDb();
            rs = s.executeQuery("SELECT * FROM tabelrequest");

            while(rs.next()) {
                Object[] row = {
                    rs.getString("nomor"),
                    rs.getString("nama"),
                    rs.getString("status"),
                };
                model3.addRow(row);
            }

        }catch(SQLException | ClassNotFoundException ex) {
            ex.printStackTrace();

        }finally {
            closeDb();
        }
    }
    
    public void loadData2(){
        model3.getDataVector().removeAllElements();
        try {
            openDb();
            rs = s.executeQuery("SELECT * FROM tabeltransaksi");

            while(rs.next()) {
                Object[] row = {
                    rs.getString("id"),
                    rs.getString("quantity"),
                    rs.getString("nama"),
                    rs.getInt("harga")
                };
                model3.addRow(row);
            }

        }catch(SQLException | ClassNotFoundException ex) {
            ex.printStackTrace();

        }finally {
            closeDb();
        }
    }

