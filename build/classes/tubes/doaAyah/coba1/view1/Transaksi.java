    private static void setupTabletransaksi() {
        model.addColumn("ID");
        model.addColumn("Quantity");
        model.addColumn("Nama");
        model.addColumn("Harga");

        try {
            openDb();
            rs = s.executeQuery("SELECT * FROM tabeltransaksi");

            while(rs.next()) {
                Object[] row = {
                    rs.getString("id"),
                    rs.getString("quantity"),
                    rs.getString("nama"),
                    rs.getInt("harga")
                };
                model.addRow(row);
            }

        }catch(SQLException | ClassNotFoundException ex) {
            ex.printStackTrace();

        }finally {
            closeDb();
        }
    }

