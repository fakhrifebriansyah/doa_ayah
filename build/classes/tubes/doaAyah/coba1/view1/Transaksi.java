    private void cancelActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_cancelActionPerformed
        // TODO add your handling code here:
        int dialogBtn = JOptionPane.YES_NO_OPTION;
        int dialogResult = JOptionPane.showConfirmDialog(this, "Anda yakin ingin membatalkan orderan?", "PERINGATAN", dialogBtn);
       if (dialogResult == 0) {
        try{
           openDb();
           try{
               String query = "DELETE FROM tabeltransaksi";
               int deletedRows=s.executeUpdate(query);
             
                if(deletedRows>0){
                   JOptionPane.showMessageDialog(null,"Pesanan Anda dibatalkan");
               }else{
                   JOptionPane.showMessageDialog(null,"Pesanan Telah dibatalkan."); 
               }
           } catch(SQLException s){
               JOptionPane.showMessageDialog(null,"Deleted All Rows In  Table Error. ");
               s.printStackTrace();
           }
        // close Connection
        c.close();
       }catch (Exception e){
           e.printStackTrace();
       }finally {
           closeDb();
       }
        super.dispose();
       }
    }//GEN-LAST:event_cancelActionPerformed

